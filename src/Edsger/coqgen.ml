  | AEindex (e', idx) ->
    output_string out ("(LCindex " ^ e.aLexprType.aTypePairIdent ^ "\n" ^ ind);
    output_lexpr out obj ("  " ^ ind) e';
    output_string out ("\n" ^ ind);
