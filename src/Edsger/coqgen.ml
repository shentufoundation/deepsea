                           (fun _ _ _ -> has_passthrough := true); *)
    iter_exporting_methods (fun n oname m -> 
			      has_fresh := true)
                           (fun n oname m -> 
			      has_passthrough := true);
    if not !has_fresh || not !has_passthrough then
